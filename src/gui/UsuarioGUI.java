    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Cadastro
        Usuario usuarios = new Usuario();
        usuarios.setNome(jTextField1.getText());
        usuarios.setCpf(jTextField2.getText());
        usuarios.setEndereco(jTextField4.getText());
        usuarios.setSexo(jTextField3.getText());
        
        if((jTextField1.getText().isEmpty())|| (jTextField2.getText().isEmpty())||(jTextField4.getText().isEmpty())||(jTextField3.getText().isEmpty())){
            JOptionPane.showMessageDialog(null,"Os Campos são obrigatorios");
        }
        else{
              UsuarioDAO dao = new UsuarioDAO();
              dao.adiciona(usuarios);
              readJTable();
              JOptionPane.showMessageDialog(null,"Usuario "+jTextField1.getText()+" Foi Cadastrado Com Sucesso!!");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

