    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if (jTable1.getSelectedRow()!= -1 ){

            UsuarioDAO dao = new UsuarioDAO();

            Usuario usuarios = new Usuario();

            usuarios.setIdcodigo((long)jTable1.getValueAt(jTable1.getSelectedRow(),0));

            usuarios.setNome(jTextField1.getText());

            usuarios.setCpf(jTextField2.getText());

            usuarios.setEndereco(jTextField4.getText());

            usuarios.setSexo(jTextField3.getText());

            dao.Update(usuarios);

            jTextField1.setText("");

            jTextField2.setText("");

            jTextField3.setText("");

            jTextField4.setText("");

            readJTable();

}
   
    }//GEN-LAST:event_jButton3ActionPerformed

