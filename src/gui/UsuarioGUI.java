    public void readJTable (){
                DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
                modelo.setNumRows(0);
                UsuarioDAO udao = new UsuarioDAO();
                for(Usuario u: udao.LEIA()){
                    modelo.addRow(new Object[]{
                        u.getIdcodigo(),
                        u.getNome(),
                        u.getCpf(),
                        u.getEndereco(),
                        u.getSexo()
                    });
                    
                }
        
        
    }
